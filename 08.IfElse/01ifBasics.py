# if 기본 예제

# if 문이 참이면 조건 실행(indentation 문장 실행)
# Indentation, 조건 문장 끝에 : 사용

temperature = 25

if temperature < 18:
  print("자켓을 입는다.")
else:
  print("자켓을 입지 않는다.")

print(type(True))  # 대문자 True 사용

if 4 < 3:
    print("Hello World")

if True :  # 항상 참인 조건
    print ("1")
    print ("2")
else :
    print("3")
print("4")
